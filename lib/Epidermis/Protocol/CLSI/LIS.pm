package Epidermis::Protocol::CLSI::LIS;
# ABSTRACT: CLSI LIS protocol for clinical laboratory equipment

use strict;
use warnings;
use namespace::autoclean;

1;

=head1 DESCRIPTION

This module implements the
Clinical and Laboratory Standards Institute (CLSI) LIS
standard protocol as specified by

=over 4

=item * L<CLSI LIS01-A2
|https://clsi.org/standards/products/automation-and-informatics/documents/lis01/>
(formerly ASTM E1381)
Specification for Low-Level Protocol to Transfer Messages Between Clinical Laboratory Instruments and Computer Systems, 2nd Edition.

=item * L<CLSI LIS02-A2
|https://clsi.org/standards/products/automation-and-informatics/documents/lis02/>
(formerly ASTM E1394)
Specification for Transferring Information Between Clinical Laboratory Instruments and Information Systems, 2nd Edition.

=back

=cut
